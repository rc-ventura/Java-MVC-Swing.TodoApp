    private void jLabelToolBarIconSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelToolBarIconSaveMouseClicked
        try {
            
            Task task = new Task();                                              //criando uma tarefa
            task.setIdProject(1);                                   // setando o id do projeto associado a tarefa 
            task.setName(jTextFieldName.getText());                             // setaando os atributos da tarefa com input da tela
            task.setDescription(jTextAreaDescription.getText());
            task.setNotes(jTextAreaNotes.getText());
            task.setCompleted(false);                                            // setando o atributo completed
            
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");   //transformando uma String para uma Data
            Date deadline = null;                                               // Usando a classe SimpleDateFormat e dando valor nulo a deadline
            
          //  try {
                deadline = dateFormat.parse(jFormattedTextFieldDeadLine.getText());    //O valor nulo recebe o input formatado
            //}catch (ParseException ex) {
              //  Logger.getLogger(TaskDialogScreen.class.getName()).log(Level.SEVERE, null, ex);
           // }

            task.setDeadline(deadline);                                         //setando o deadline para o objeto tarefa
                                            
            taskController.save(task);                                           // salvando a tarefa

            JOptionPane.showMessageDialog(rootPane, "Tarefa salva com sucesso");   //mensagem de sucesso em uma nova tela
            this.dispose();                                                      //fechando a tela
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jLabelToolBarIconSaveMouseClicked

